num = 85

num += 10  # num = num + 10
print("num += 10 后, num = ", num)  # 95

num -= 10  # num = num - 10
print("num -= 10 后, num = ", num)#85

num *= 10  # num = num * 10
print("num *= 10 后, num = ", num)#850

# num /= 10  # num = num / 10
print("num /= 10 后, num = ", num)#除法运算的结果是浮点数

num //= 10  # num = num // 10
print("num //= 10 后, num = ", num)

num %= 3  # num = num % 3
print("num %= 3 后, num = ", num)

num **= 3  # num = num ** 3
print("num **= 3 后, num = ", num)